using ScriptCoreLib;

namespace java.io
{
    [Script(IsNative = true)]
    public class OutputStream
    {
        #region methods
        /// <summary>
        /// Closes this output stream and releases any system resources  associated with this stream.
        /// </summary>
        public void close()
        {
        }

        /// <summary>
        /// Flushes this output stream and forces any buffered output bytes  to be written out.
        /// </summary>
        public void flush()
        {
        }

        /// <summary>
        /// Writes <code>b.length</code> bytes from the specified byte array  to this output stream.
        /// </summary>
        public void write(sbyte[] b)
        {
        }

        /// <summary>
        /// Writes <code>len</code> bytes from the specified byte array  starting at offset <code>off</code> to this output stream.
        /// </summary>
        public void write(sbyte[] b, int off, int len)
        {
        }

        /// <summary>
        /// Writes the specified byte to this output stream.
        /// </summary>
        public void write(int b)
        {
        }

        #endregion

    }
}
